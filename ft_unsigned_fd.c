#include "ft_printf.h"

int	ft_unsigned_fd(unsigned int n)
{
	int	i;

	i = 0;
	if (n >= 10)
	{
		i += ft_unsigned_fd(n / 10);
		n %= 10;
	}
	if (n < 10)
	{
		i++;
		ft_putchar_fd(n + 48);
	}
	return (i);
}
